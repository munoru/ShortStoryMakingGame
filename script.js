// script.js
document.addEventListener('DOMContentLoaded', () => {
    const generateButton = document.getElementById('generate');
    const wordList = document.getElementById('wordList');
    const historyContainer = document.querySelector('footer ul');

    generateButton.addEventListener('click', generateRandomWords);

    function generateRandomWords() {
        // 既存の単語リストからランダムな単語を選択
        let selectedWords = pickRandomWords(words, 5);
        displayWords(selectedWords);
        addToHistory(selectedWords);
    }

    function displayWords(wordsArray) {
        // 単語リストの表示をクリア
        wordList.innerHTML = '';
        // 新しい単語を表示
        wordsArray.forEach(word => {
            let wordDiv = document.createElement('div');
            wordDiv.textContent = word;
            wordList.appendChild(wordDiv);
        });
    }

    function addToHistory(wordsArray) {
        // 履歴に新しいグループを追加
        let historyEntry = document.createElement('li');
        historyEntry.textContent = wordsArray.join(', ');
        historyContainer.prepend(historyEntry); // 新しいエントリを最初に追加
    }

    function pickRandomWords(arr, count) {
        // 配列からランダムに要素を選択
        let shuffled = arr.sort(() => 0.5 - Math.random());
        return shuffled.slice(0, count);
    }
});
